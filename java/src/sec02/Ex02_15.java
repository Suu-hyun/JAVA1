package sec02;

public class Ex02_15 {
    public static void main(String[] args) {
        char alphaA = 'A'; // character는 꼭 '' 넣어줌(""이건 안됨) 그리고 문자열 한개만 반드시(여러개 안됨)
        char alphaB = 'B';
        System.out.println(alphaA); // A
        System.out.println(alphaB); // B
        System.out.println(alphaA + alphaB); // 131
    }
}
