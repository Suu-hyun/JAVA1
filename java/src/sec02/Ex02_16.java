package sec02;

public class Ex02_16 {
    public static void main(String[] args) {
        int alphaA = 'A'; // 문자 A의 코드 포인트가 할당됨
        int alphaB = 'B';
        System.out.println(alphaA); // 65
        System.out.println(alphaB); // 66
        System.out.println(alphaA + alphaB); // 131
        System.out.println(alphaA * alphaB); // 4290
    }
}
