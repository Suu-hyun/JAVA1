package sec02;

public class Ex02_12 {
    public static void main(String[] args) {
        byte num1 = 73; // 십진수
        System.out.println(num1);

        byte num2 = 0b1001000; // 2진수
        System.out.println(num2);

        byte num3 = 0107; // 8진수
        System.out.println(num3);
        
        byte num4 = 0x46; // 16진수
        System.out.println(num4);
    }
}
