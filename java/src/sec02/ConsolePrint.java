package sec02;

public class ConsolePrint {
    public static void main(String[] args) {
        // 줄바꿈 하지 않음
        System.out.print("안녕!");
        System.out.print("자바 월드!");

        // 줄바꿈 처리
        System.out.println();
        System.out.println("안녕!");
        System.out.println("자바 월드!");
    }
}
