package sec02;

public class Ex02_07 {
    public static void main(String[] args) {
        // 정적 타이핑(static typing)
        // 자료(data) 형식(type)
        // 정수(integer) : 소수점 없는 숫자
        int age = 10; // 대입(할당) 연산자

        // 변수 정의
        String name; // 변수 선언
        name = "김일남"; // 변수 초기화

        System.out.println(age);
        System.out.println(name);
    }
}
