package sec03;

public class Ex03_13 {
    public static void main(String[] args) {
        // 관계(비교) 연산자
        // > >= < <= == !=
        // 평가 결과는 논리형(true, false)
        
        System.out.println(1 > 2); // false
        System.out.println(2 >= 2); // true
        System.out.println(1 < 2); // true
        System.out.println(2 <= 2); // true
        System.out.println(2 == 2); // true
        System.out.println(2 != 2); // false
    }
}
