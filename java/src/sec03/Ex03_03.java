package sec03;

public class Ex03_03 {
    public static void main(String[] args) {
        int num = 1;
        System.out.println(num); // 1
        
        // 증감 연산자(++, --)
        num++; // 기존 값에 1 증가 (증가 연산자)
        System.out.println(num); // 2
        
        num--; // 기존 값에 1 감소 (감소 연산자)
        System.out.println(num); // 1
    }
}
